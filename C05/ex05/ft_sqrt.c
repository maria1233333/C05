int	ft_sqrt(int nb)
{
	long	i;
	long	num;

	if (nb <= 0)
		return (0);
	i = 1;
	num = (long)nb;
	while (i * i <= num)
	{
		if (i * i == num)
			return ((int)i);
		i++;
	}
	return (0);
}
